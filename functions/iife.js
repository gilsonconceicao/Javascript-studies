//IIFE - Immediately Imvoked Function Expression 
/* é uma forma de aplicar uma função sem que ela acesse a escopo mais abrangente, podendo e deve ter um retorno imediato. 
*/

(function() {
    console.log('Aqui é uma ótima forma de proteger a sua função no código.'); 
    console.log('Ou seja, definindo e autoinvocando.')
})()