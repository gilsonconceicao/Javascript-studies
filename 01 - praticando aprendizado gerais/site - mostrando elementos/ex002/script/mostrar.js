// mostrar elementos 

let botao01 = window.document.getElementById('botao01'); 

botao01.addEventListener('click', function() {
    let cont01 = window.document.getElementById('cont01'); 

    if (cont01.style.display === 'block') {
        cont01.style.display = 'none';
    } else {
        cont01.style.display = 'block';
    }
});


